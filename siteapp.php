    <p>Your decision on developing a native mobile application or a responsive web application relies on the context of your project. 
    Both types of applications have their merits. Rather than looking at the pros and cons of both, it may be more useful looking 
    at what features you can take advantage of to support the growth and purpose of your application.</p>

    <p>Native mobile applications tout the ability to work offline, capitalize app store monetization, and can leverage mobile device 
    features. One of the more prominent features mobile apps have to offer is how user customizable they are. Chris Ciligot of 
    clearbridgemobile.com states, “Mobile apps allow users to set up their preferences as soon as the app is downloaded, and 
    customize it to suit their needs. Apps can also track user engagement, and use it to offer custom recommendations and updates, 
    making the app more useful to the user (Ciligot).” If the needs of your project are more personal, food or fitness journals, 
    then developing a mobile application may be favorable.</p>

    <p>Responsive websites and web applications are cheaper, easier to maintain, and are compatible across multiple devices; 
    compatibility being the strongest feature of the three. Tania H. of rubygarage.org writes, “Due to the multi-device support 
    that responsive web design provides across various platforms, it's becoming easy to reach a broader audience than a mobile 
    app can reach (Tania).” Having a broad audience may be useful for projects that want to inform readers like the news. 
    If the user device supports the use of an internet browser, promotional material can make use of multi-device support. 
    Portfolios or small business websites for example.</p>

    <p>Responsive websites and mobile applications can be likened to tools that serve different purposes. If the purpose of your 
    project is to cater a user experience, you can use the customization aspects of mobile applications. If your goal is to 
    capture a large audience, take advantage of the multi-device support responsive websites have to offer.</p>

